const AUTHORITY_TO_ENGINE = {
  'Emotional Authority': 'Wave Engine',
  'Sacral Authority': 'Response Engine',
  'Splenic Authority': 'Instinct Engine',
  'Ego Manifested Authority': 'Will Engine',
  'Ego Projected Authority': 'Will Engine',
  'Ego Authority': 'Will Engine',
  'Will Authority': 'Will Engine',
  'Self-Projected Authority': 'Voice Engine',
  'Mental / Environmental Authority': 'Resonance Engine',
  'Mental Authority': 'Resonance Engine',
  'Environmental Authority': 'Resonance Engine',
  'Lunar Authority': 'Cycle Engine',
};

export function getDecisionSupportForRhythm(authorityLabel, rhythmName) {
  const engineName = AUTHORITY_TO_ENGINE[authorityLabel];
  const engine = engineName ? RHYTHM_DECISION_SUPPORT[engineName] : null;
  const rhythm = engine?.rhythms?.[rhythmName] || null;

  if (engine && rhythm) {
    return {
      authorityLabel: authorityLabel || engine.authorityType,
      authorityEngineName: engineName,
      interactionType: engine.interactionType,
      supportText: engine.supportText,
      slogan: rhythm.slogan,
      bestUseText: rhythm.bestUse,
      watchOutText: rhythm.watchOut,
    };
  }

  return {
    authorityLabel: authorityLabel || 'Decision Engine',
    authorityEngineName: engineName || null,
    interactionType: 'Decision support',
    supportText: 'Today\'s rhythm changes the pace and texture of your clarity. Use the field for context, but let your core decision engine stay in charge.',
    slogan: null,
    bestUseText: 'Use the rhythm as context, not a replacement for your own clarity process.',
    watchOutText: 'Letting the day’s energy override the way your decisions actually work.',
  };
}
