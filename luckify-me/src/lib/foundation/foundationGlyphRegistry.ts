const keySet = new Set<string>(FOUNDATION_GLYPH_KEYS);
const dynamicNameToKey = Object.values(foundationGlyphRegistry).reduce<Record<string, FoundationGlyphKey>>((acc, entry) => {
  acc[entry.dynamicName.toLowerCase()] = entry.key;
  return acc;
}, {});

export function isFoundationGlyphKey(value: unknown): value is FoundationGlyphKey {
  return typeof value === 'string' && keySet.has(value);
}

export function slugifyFoundationDynamicName(value: string): string {
  return value
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

export function getFoundationGlyphKey(input?: string | null): FoundationGlyphKey | null {
  if (!input) return null;
  if (isFoundationGlyphKey(input)) return input;

  const normalizedName = input.trim().toLowerCase();
  if (dynamicNameToKey[normalizedName]) return dynamicNameToKey[normalizedName];

  const slug = slugifyFoundationDynamicName(input);
  return isFoundationGlyphKey(slug) ? slug : null;
}

export function resolveFoundationGlyphKey(
  input?: string | null,
  fallback: FoundationGlyphKey = 'the-pioneer',
): FoundationGlyphKey {
  return getFoundationGlyphKey(input) || fallback;
}
